import 'package:flutter/material.dart';

void main() {
  runApp(
    MaterialApp(
      debugShowCheckedModeBanner: false,
      theme: ThemeData(primarySwatch: Colors.blueGrey),
      home: ResistorDetailPage(),
    ),
  );
}

class ResistorDetailPage extends StatelessWidget {
  final List<String> resistorItems = [
    'Carbon Film Resistor',
    'Current Sense Resistor',
    'High Voltage Resistor',
    'LED Strip Resistor',
    'Metal Glaze Resistor',
    'Metal Oxide Resistor',
    'Potentiometers Resistor',
    'Network & Arrays Resistor',
    'Variable Resistor',
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: Text("Resistors")),
      body: ListView.builder(
        itemCount: resistorItems.length,
        padding: const EdgeInsets.all(16),
        itemBuilder: (context, index) {
          return Card(
            elevation: 4,
            margin: const EdgeInsets.symmetric(vertical: 8),
            shape: RoundedRectangleBorder(
              borderRadius: BorderRadius.circular(12),
            ),
            child: ListTile(
              title: Text(resistorItems[index]),
              trailing: const Icon(Icons.arrow_forward_ios),
              onTap: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(
                    builder: (_) => _getResistorPage(resistorItems[index]),
                  ),
                );
              },
            ),
          );
        },
      ),
    );
  }

  Widget _getResistorPage(String name) {
    switch (name) {
      case 'Carbon Film Resistor':
        return CarbonFilmResistorPage();
      case 'Current Sense Resistor':
        return CurrentSenseResistorsPage();
      case 'High Voltage Resistor':
        return HighVoltageResistorPage();
      case 'LED Strip Resistor':
        return LEDStripResistorsPage();
      case 'Metal Glaze Resistor':
        return MetalGlazeResistorsPage();
      case 'Metal Oxide Resistor':
        return MetalOxideResistorPage();
      case 'Potentiometers Resistor':
        return PotentiometersResistorsPage();
      case 'Network & Arrays Resistor':
        return NetworkArraysResistorsPage();
      case 'Variable Resistor':
        return VariableResistorsPage();
      default:
        return IndividualResistorPage(
          title: name,
          description: 'No description available.',
          imagePath: 'assets/resistors/default.png',
        );
    }
  }
}

